    private void jButtonCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCadastrarActionPerformed

        int diaC, mesC, anoC, diaN, mesN, anoN;
        diaC = Integer.parseInt(txtCadDia.getText());
        mesC = Integer.parseInt(txtCadMes.getText());
        anoC = Integer.parseInt(txtCadAno.getText());
        diaN = Integer.parseInt(txtNasDia.getText());
        mesN = Integer.parseInt(txtNasMes.getText());
        anoN = Integer.parseInt(txtNasAno.getText());

        Endereco endereco = new Endereco();
        endereco.setRua(txtRua.getText());
        endereco.setNumero(Integer.parseInt(txtNumero.getText()));
        endereco.setBairro(txtBairro.getText());
        endereco.setComplemento(txtComplemento.getText());
        endereco.setCidade(txtCidade.getText());
        endereco.setCep(txtCep.getText());
        endereco.setEstado(txtEstado.getText());


        Cliente clienteNovo = new Cliente(txtNome.getText(), txtCpf.getText(), txtTelefone.getText(), (String)ComboBoxSexo.getSelectedItem(), new Endereco(), new Data(diaN,mesN,anoN), new Data(diaC,mesC,anoC));
        clienteNovo.setEndereco(endereco);

        if(Game.cadastrarPessoa(clienteNovo) == true){

            txtStatus.setText("Cadastro ConcluÃ­do!");
        }
        else{
            txtStatus.setText("Erro: CPF existente");
        }

    }//GEN-LAST:event_jButtonCadastrarActionPerformed

